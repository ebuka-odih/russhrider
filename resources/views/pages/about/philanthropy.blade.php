                                <h3 style="text-align: center;">Pink Ribbon Girls</h3>
                                <p>Have you seen the pink Rush truck driving around? Rush has decided to go pink in honor of partnering with Pink Ribbon Girls! Pink Ribbon Girls is an organization that offers many free and direct services to breast cancer patients or survivors and their families. Through their Simply Fight! And NO Age NO Stage programs, services include meals, housekeeping, transportation, and peer support.</p>
                                <p>What role does Rush play with this amazing organization? Rush is delivering all of the meals to the patients and their families. We are delighted to have the opportunity to partner with Pink Ribbon Girls and have a part in this incredible cause.</p>
                                <p>To find out more about Pink Ribbon Girls or discover more ways that you can help visit <a href="https://www.pinkribbongirls.org/">pinkribbongirls.org</a></p>
                                <h3 style="text-align: center;">Coats for Kids</h3>
                                <p>Rush is teaming up with Coats for Kids once again! Coats for Kids is an annual campaign that collects new and gently used coats and distributes them to people in need in the Dayton area. They have collected over 485,000 coats since the campaign began 32 years ago.</p>
                                <p>Like in years past, Rush will be handling the transportation of the coat collection barrels from the various coat drop off locations to the distribution location.</p>
                                <p>For more information and a list of drop off locations visit the link <a href="http://wdtn.com/2015/09/17/coats-for-kids-2015/" class="broken_link">here.</a></p>
                                <h3 style="text-align: center;">Delivering School Supplies to Potter’s House</h3>
                                <p>The school year is officially in full swing which means that there are countless children in need of backpacks and other school supplies. On August 22, Rush handled the pickup and delivery of 600 backpacks from Fairhaven Church to Potter’s House as a part of the Celebrate Dayton Innerwest Block Party. Rush would like to thank Fairhaven Church for working towards providing local children with these supplies as well as including Rush in on the cause.</p>
